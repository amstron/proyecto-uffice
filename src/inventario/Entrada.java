    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        /**
         * Agregar productos a la entrada del inventario
         */
        PreparedStatement ps;
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String date = sdf.format(jDateChooser1.getDate());
        try {
            ps = cn.prepareStatement("INSERT INTO Entrada VALUES (?,?,?,?,?)");
            ps.setString(1, date);
            ps.setFloat(2, Float.parseFloat(jTextField1.getText()));
            ps.setString(3, jTextField2.getText());
            ps.setFloat(4, Float.parseFloat(jTextField3.getText()));
            ps.setInt(5, Integer.parseInt(jTextField4.getText()));
            int res = ps.executeUpdate();
            if (res == 1) {
                JOptionPane.showMessageDialog(null, "Datos guardados.");
                dispose();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


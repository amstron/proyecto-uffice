public class Gerencia {
    
    private static int ebitda;
    private static int proyecto;
    private static int margen;
    private static int resultado;
    private static int reporte;

    /**
     * @return the ebitda
     */
    public static int getEbitda() {
        return ebitda;
    }

    /**
     * @param aEbitda the ebitda to set
     */
    public static void setEbitda(int aEbitda) {
        ebitda = aEbitda;
    }

    /**
     * @return the proyecto
     */
    public static int getProyecto() {
        return proyecto;
    }

    /**
     * @param aProyecto the proyecto to set
     */
    public static void setProyecto(int aProyecto) {
        proyecto = aProyecto;
    }

    /**
     * @return the resultado
     */
    public static int getResultado() {
        return resultado;
    }

    /**
     * @param aResultado the resultado to set
     */
    public static void setResultado(int aResultado) {
        resultado = aResultado;
    }

    /**
     * @return the margen
     */
    public static int getMargen() {
        return margen;
    }

    /**
     * @param aMargen the margen to set
     */
    public static void setMargen(int aMargen) {
        margen = aMargen;
    }

    /**
     * @return the reporte
     */
    public static int getReporte() {
        return reporte;
    }

    /**
     * @param aReporte the reporte to set
     */
    public static void setReporte(int aReporte) {
        reporte = aReporte;
    }
    
}

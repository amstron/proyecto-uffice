
package lib;

/**
 * @author GAES 2
 */

public class Proveedor {

    private static String nombre;
    private static String nit;
    private static String ciudad;
    private static String dir;
    private static String tel;

    /**
     * @return the nombre
     */
    public static String getNombre() {
        return nombre;
    }

    /**
     * @param aNombre the nombre to set
     */
    public static void setNombre(String aNombre) {
        nombre = aNombre;
    }

    /**
     * @return the nit
     */
    public static String getNit() {
        return nit;
    }

    /**
     * @param aNit the nit to set
     */
    public static void setNit(String aNit) {
        nit = aNit;
    }

    /**
     * @return the ciudad
     */
    public static String getCiudad() {
        return ciudad;
    }

    /**
     * @param aCiudad the ciudad to set
     */
    public static void setCiudad(String aCiudad) {
        ciudad = aCiudad;
    }

    /**
     * @return the dir
     */
    public static String getDir() {
        return dir;
    }

    /**
     * @param aDir the dir to set
     */
    public static void setDir(String aDir) {
        dir = aDir;
    }

    /**
     * @return the tel
     */
    public static String getTel() {
        return tel;
    }

    /**
     * @param aTel the tel to set
     */
    public static void setTel(String aTel) {
        tel = aTel;
    }
}

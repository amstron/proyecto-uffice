
package lib;

/**
 * @author GAES 2
 */

public class Producto {
    
    private static String codigo;
    private static String nombre;
    private static String marca;
    private static String medida;
    private static String proveedor;
    private static String descripcion;
    private static String fecha;
    private static double cantidad;
    private static String detalle;
    private static int precio;
    

    /**
     * @return the codigo
     */
    public static String getCodigo() {
        return codigo;
    }

    /**
     * @param aCodigo the codigo to set
     */
    public static void setCodigo(String aCodigo) {
        codigo = aCodigo;
    }

    /**
     * @return the nombre
     */
    public static String getNombre() {
        return nombre;
    }

    /**
     * @param aNombre the nombre to set
     */
    public static void setNombre(String aNombre) {
        nombre = aNombre;
    }

    /**
     * @return the marca
     */
    public static String getMarca() {
        return marca;
    }

    /**
     * @param aMarca the marca to set
     */
    public static void setMarca(String aMarca) {
        marca = aMarca;
    }

    /**
     * @return the medida
     */
    public static String getMedida() {
        return medida;
    }

    /**
     * @param aMedida the medida to set
     */
    public static void setMedida(String aMedida) {
        medida = aMedida;
    }

    /**
     * @return the proveedor
     */
    public static String getProveedor() {
        return proveedor;
    }

    /**
     * @param aProveedor the proveedor to set
     */
    public static void setProveedor(String aProveedor) {
        proveedor = aProveedor;
    }

    /**
     * @return the descripcion
     */
    public static String getDescripcion() {
        return descripcion;
    }

    /**
     * @param aDescripcion the descripcion to set
     */
    public static void setDescripcion(String aDescripcion) {
        descripcion = aDescripcion;
    }

    /**
     * @return the fecha
     */
    public static String getFecha() {
        return fecha;
    }

    /**
     * @param aFecha the fecha to set
     */
    public static void setFecha(String aFecha) {
        fecha = aFecha;
    }

    /**
     * @return the cantidad
     */
    public static double getCantidad() {
        return cantidad;
    }

    /**
     * @param aCantidad the cantidad to set
     */
    public static void setCantidad(double aCantidad) {
        cantidad = aCantidad;
    }

    /**
     * @return the detalle
     */
    public static String getDetalle() {
        return detalle;
    }

    /**
     * @param aDetalle the detalle to set
     */
    public static void setDetalle(String aDetalle) {
        detalle = aDetalle;
    }

    /**
     * @return the precio
     */
    public static int getPrecio() {
        return precio;
    }

    /**
     * @param aPrecio the precio to set
     */
    public static void setPrecio(int aPrecio) {
        precio = aPrecio;
    }
    
}
